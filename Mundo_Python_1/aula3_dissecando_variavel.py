word = input('Digite lguma coisa: ')

print('o tipo primivo é? ', type(word))
print('o que foi digitado é um numero? ', word.isnumeric())
print('o que foi digitado é um letra? ', word.isalpha())
print('o que foi digitado está tudo em letras minuscula? ', word.islower())
print('o que foi digitado está tudo em letras maiuscula? ', word.isupper())

